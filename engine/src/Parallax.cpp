#include "Parallax.hpp"

namespace parallax {

    Application &init()
    {
        Application &app = Application::getInstance();
        app.init();
        LOG(PARALLAX_INFO, "Parallax engine initialized");
        return app;
    }

    Application &getApp()
    {
        return Application::getInstance();
    }

    void runEngine(const Application::SceneInfo &sceneInfo)
    {
        Application &app = Application::getInstance();
        app.run(sceneInfo);
    }

}
